#!/usr/bin/env node

/**
 * Scans docs/src/content/docs/ for .mdx/.md files and generates a typed
 * DocsPath union so the web app gets build-time safety for docs links.
 *
 * Run: pnpm docs:paths
 */

import { readdirSync, statSync, writeFileSync } from "fs";
import { join, relative, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const DOCS_DIR = join(__dirname, "../docs/src/content/docs");
const OUTPUT = join(__dirname, "../packages/web/src/lib/docs-paths.generated.ts");

function collectPaths(dir) {
  const paths = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) {
      paths.push(...collectPaths(full));
    } else if (entry.endsWith(".mdx") || entry.endsWith(".md")) {
      const rel = relative(DOCS_DIR, full).replace(/\.(mdx|md)$/, "");
      // Skip the root index page — it's not a linkable docs path
      if (rel !== "index") paths.push(rel);
    }
  }
  return paths;
}

const paths = collectPaths(DOCS_DIR).sort();

const content = `// AUTO-GENERATED by scripts/generate-docs-paths.mjs — do not edit manually.
// Run: pnpm docs:paths

export const DOCS_PATHS = [
${paths.map((p) => `  "${p}",`).join("\n")}
] as const;

export type DocsPath = (typeof DOCS_PATHS)[number];
`;

writeFileSync(OUTPUT, content);
console.log(`Generated ${paths.length} docs paths → ${OUTPUT}`);
